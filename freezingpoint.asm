exit
oldSP equ $+1
	ld sp,0
	pop hl
	exx
	ei
	ret

;*******************************************************************************
rdptn0
	ld (ptnpntr),de

readPtn
	in a,($fe)		;read kbd
	cpl
	and $1f
	jr nz,exit


ptnpntr equ $+1
	ld sp,0	
	
	pop af			;timer + ctrl
	jr z,rdseq
	
	ld b,a			;timer ($ ticks)
	
	jr c,_noUpd1
	
	ex af,af'
	
	ld h,HIGH(mixAlgo)
	pop de
	ld a,d
	
	and $f8
	ld l,a
	
	ld a,(hl)
