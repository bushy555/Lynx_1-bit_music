	cp $80			;7		;volume
	sbc a,a			;4
				
;	or $7			;7		;border
;	out ($fe),a		;11
	sla	a
	ld	($6800), a

	djnz sloop		;13/8

	dec c			;4
	jr nz,sloop		;12

	jr drumEnd
	
drum1						;kick
	ld (deRest),de
	ld (bcRest),bc
	ld (hlRest),hl

